    private void tblMedicamentosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblMedicamentosKeyReleased
        int v=0;
        double soma=0;
        if (evt.getKeyCode() == 8) {
            DefaultTableModel modelo = (DefaultTableModel) tblvendas.getModel();
            modelo.setNumRows(cont--);   
            medicamentos.clear();
        }
        if(evt.getKeyCode()==38 || evt.getKeyCode()==40) {
                for (int i = 0; i < medicamentos.size(); i++) {
                     
                      v=v+Integer.parseInt(tblvendas.getModel().getValueAt(i, 4).toString());
                      soma=soma+(Double.parseDouble(tblvendas.getModel().getValueAt(i, 0).toString())*Integer.parseInt(tblvendas.getModel().getValueAt(i, 4).toString()));
            }
                  label_total.setText(String.valueOf(v));
                  lb_soma.setText(String.valueOf(soma));
        }
    }//GEN-LAST:event_tblMedicamentosKeyReleased

