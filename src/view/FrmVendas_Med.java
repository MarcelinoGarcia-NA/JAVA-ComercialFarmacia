    private void btnPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisaActionPerformed

        Medicamento n = new Medicamento();
            n = controlmed.buscaMedicamento(txt_pesquisa.getText());
            if(n.getId()==0){
                JOptionPane.showMessageDialog(null,"Medicamento não encontrado!");
             }else{
            medicamentos.add(n);
            tblvendas.getColumnModel().getColumn(0).setPreferredWidth(5);
            tblvendas.getColumnModel().getColumn(1).setPreferredWidth(40);
            tblvendas.getColumnModel().getColumn(2).setPreferredWidth(40);
            tblvendas.getColumnModel().getColumn(3).setPreferredWidth(15);
            tblvendas.getColumnModel().getColumn(4).setPreferredWidth(15);

            DefaultTableModel modelo = (DefaultTableModel) tblvendas.getModel();
            modelo.setNumRows(0);
            double soma=0;
            int v=0;
            cont++;
            for (int i = 0; i < medicamentos.size(); i++) {
                   
                    modelo.addRow(new Object[]{
                        medicamentos.get(i).getValor_med(),
                        medicamentos.get(i).getNome_med(),
                        medicamentos.get(i).getFabricante(),
                        medicamentos.get(i).getCod_barras(),
                         1
                    });
                      soma=soma+Double.parseDouble(tblvendas.getModel().getValueAt(i, 0).toString());
                      v=v+Integer.parseInt(tblvendas.getModel().getValueAt(i, 4).toString());
            }
            
                label_total.setText(String.valueOf(v));
                lb_soma.setText(String.valueOf(soma));
            }
    }//GEN-LAST:event_btnPesquisaActionPerformed

