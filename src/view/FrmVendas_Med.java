    private void btn_finalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_finalizarActionPerformed
        int opcao = JOptionPane.showConfirmDialog(null,"Deseja finalizar Venda?");
        if (opcao ==0){
            for (int i = 0; i < medicamentos.size(); i++) {
                int quant=Integer.parseInt(tblvendas.getModel().getValueAt(i, 4).toString());
                controlmed.alterarMedicamento(medicamentos.get(i).getId(),medicamentos.get(i).getQuant()-quant);
            }
            String x="", y="",z="";
            for (int i = 0; i < medicamentos.size(); i++) {
                x=medicamentos.get(i).getNome_med()+" "+x;
                y=medicamentos.get(i).getCod_barras()+" "+y;
                z=medicamentos.get(i).getFabricante()+" "+z;
    
            }
            Medicamento med_salvo= new Medicamento();
            med_salvo.setNome_med(x);
            med_salvo.setCod_barras(y);
            med_salvo.setClasse_med(6);
            med_salvo.setQuant(Integer.parseInt(label_total.getText()));
            med_salvo.setValor_med(Double.parseDouble(lb_soma.getText()));
            med_salvo.setFabricante(z);

            controlmed.salvarVenda(med_salvo);
            medicamentos.clear();
            DefaultTableModel modelo = (DefaultTableModel) tblvendas.getModel();
            modelo.setNumRows(0);
            txt_pesquisa.setText("");
            label_total.setText("0");
            lb_soma.setText("");
            JOptionPane.showMessageDialog(null,"Venda efetivada");
            
           
        }else{
            JOptionPane.showMessageDialog(null,"Venda não efetivada");
        }   
    }//GEN-LAST:event_btn_finalizarActionPerformed

